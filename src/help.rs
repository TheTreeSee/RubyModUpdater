pub fn print_help() {
    println!("Usage: RubyModManager [OPTIONS]");
    println!();
    println!("OPTIONS:");
    println!("  -h, --help      Print this help message");
    println!("  -i, --init      Create the mods folder if it doesn't exist");
    println!("  -f, --folder    Display or Set the path to the mods folder (default: current directory)");
}